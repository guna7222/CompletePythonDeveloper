# positional arguments
# keyword arguments
# default parameters
def audi(model='audi-8', color ='pure black', price='2.4 cr'):
    print(f'hello model {model} price {price} color {color}')


# positional arguments
audi(19121, 'x', 'white')
# keyword arguments
audi(model='x', color='white', price=12132)

# default parameters
audi()